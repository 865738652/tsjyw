package edu.iasd.form;

import edu.iasd.pojo.Student;

public class StudentForm extends UserForm {
	
	private int schoolClassId;
	private int studentId;
	private int studentStateId;
	private String studentCode;
	
	public int getSchoolClassId() {
		return schoolClassId;
	}
	public void setSchoolClassId(int schoolClassId) {
		this.schoolClassId = schoolClassId;
	}
	public int getStudentId() {
		return studentId;
	}
	public void setStudentId(int studentId) {
		this.studentId = studentId;
	}
	public int getStudentStateId() {
		return studentStateId;
	}
	public void setStudentStateId(int studentStateId) {
		this.studentStateId = studentStateId;
	}
	public String getStudentCode() {
		return studentCode;
	}
	public void setStudentCode(String studentCode) {
		this.studentCode = studentCode;
	}
	public void fillStudent(Student s) {
		s.setStudentCode(getStudentCode());
		
	}
	
	
	

}
