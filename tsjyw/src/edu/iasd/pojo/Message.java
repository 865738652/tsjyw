package edu.iasd.pojo;

public class Message {
	
	
	public static final Integer MESSAGE_ARTICLE = 1;
	public static final Integer MESSAGE_NOTICE = 2;
	
	public static final Integer MESSAGE_SENDNUM = 4;
	
	public static final Integer MESSAGE_STATE_WAIT = 0;
	public static final Integer MESSAGE_STATE_START = 1;
	
	
	private Integer messageId;
	private Integer wechatMessageId;
	private Integer messageTypeId;
	private Integer messageStateId;
	
	
	
	public Integer getMessageId() {
		return messageId;
	}
	public void setMessageId(Integer messageId) {
		this.messageId = messageId;
	}
	public Integer getMessageTypeId() {
		return messageTypeId;
	}
	public void setMessageTypeId(Integer messageTypeId) {
		this.messageTypeId = messageTypeId;
	}
	public Integer getWechatMessageId() {
		return wechatMessageId;
	}
	public void setWechatMessageId(Integer wechatMessageId) {
		this.wechatMessageId = wechatMessageId;
	}
	public Integer getMessageStateId() {
		return messageStateId;
	}
	public void setMessageStateId(Integer messageStateId) {
		this.messageStateId = messageStateId;
	}

	
}
