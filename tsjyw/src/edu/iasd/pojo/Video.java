package edu.iasd.pojo;
// default package



/**
 * Video entity. @author MyEclipse Persistence Tools
 */

public class Video  implements java.io.Serializable {


    // Fields    

     private Integer videoId;
     private Integer articleId;
     private String videoIntro;
     private String videoPath;
     private Integer videoSerial;
     private String videoName;
     private Float videoSize;


    // Constructors

    /** default constructor */
    public Video() {
    }

    
    /** full constructor */
    public Video(Integer articleId, String videoIntro, String videoPath) {
        this.articleId = articleId;
        this.videoIntro = videoIntro;
        this.videoPath = videoPath;
    }

   
    // Property accessors

    public Integer getVideoId() {
        return this.videoId;
    }
    
    public void setVideoId(Integer videoId) {
        this.videoId = videoId;
    }

    public Integer getArticleId() {
		return articleId;
	}

	public void setArticleId(Integer articleId) {
		this.articleId = articleId;
	}

    public String getVideoIntro() {
        return this.videoIntro;
    }
    
    public void setVideoIntro(String videoIntro) {
        this.videoIntro = videoIntro;
    }

    public String getVideoPath() {
        return this.videoPath;
    }
    
    public void setVideoPath(String videoPath) {
        this.videoPath = videoPath;
    }


	public Integer getVideoSerial() {
		return videoSerial;
	}


	public void setVideoSerial(Integer videoSerial) {
		this.videoSerial = videoSerial;
	}


	public String getVideoName() {
		return videoName;
	}


	public void setVideoName(String videoName) {
		this.videoName = videoName;
	}


	public Float getVideoSize() {
		return videoSize;
	}


	public void setVideoSize(Float videoSize) {
		this.videoSize = videoSize;
	}    
}