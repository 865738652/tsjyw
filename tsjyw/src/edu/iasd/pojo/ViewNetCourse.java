package edu.iasd.pojo;

import java.io.Serializable;
import java.sql.Timestamp;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name="v_netcourse")
public class ViewNetCourse extends ViewUserBase{

	private static final long serialVersionUID = -7674269980281525366L;
	@Id
	@Column
	private Integer netCourseId;
	
	
	@Column
	private String netCourseName;
	
	@Column
	private Timestamp netCourseTime;
	
	@Column
	private String netCourseLink;
	
	@Column
	private String netCourseImgPath;
	
	@Column
	private String netCourseIntro;
	
	@Column
	private Integer netCourseTypeId;
	
	
	@Column
	private String netCourseTypeName;
	
	@Column
	private Integer netCourseIntegral;
	
	@Column
	private String netCourseNumber;
	
	@Column
	private Integer userId;
	
	public String getNetCourseNumber() {
		return netCourseNumber;
	}

	public void setNetCourseNumber(String netCourseNumber) {
		this.netCourseNumber = netCourseNumber;
	}

	public Integer getNetCourseId() {
		return netCourseId;
	}

	public void setNetCourseId(Integer netCourseId) {
		this.netCourseId = netCourseId;
	}

	

	public static long getSerialversionuid() {
		return serialVersionUID;
	}

	public String getNetCourseName() {
		return netCourseName;
	}

	public void setNetCourseName(String netCourseName) {
		this.netCourseName = netCourseName;
	}

	public Timestamp getNetCourseTime() {
		return netCourseTime;
	}

	public void setNetCourseTime(Timestamp netCourseTime) {
		this.netCourseTime = netCourseTime;
	}

	public String getNetCourseLink() {
		return netCourseLink;
	}

	public void setNetCourseLink(String netCourseLink) {
		this.netCourseLink = netCourseLink;
	}

	public String getNetCourseImgPath() {
		return netCourseImgPath;
	}

	public void setNetCourseImgPath(String netCourseImgPath) {
		this.netCourseImgPath = netCourseImgPath;
	}

	public String getNetCourseIntro() {
		return netCourseIntro;
	}

	public void setNetCourseIntro(String netCourseIntro) {
		this.netCourseIntro = netCourseIntro;
	}

	public Integer getNetCourseTypeId() {
		return netCourseTypeId;
	}

	public void setNetCourseTypeId(Integer netCourseTypeId) {
		this.netCourseTypeId = netCourseTypeId;
	}

	public String getNetCourseTypeName() {
		return netCourseTypeName;
	}

	public void setNetCourseTypeName(String netCourseTypeName) {
		this.netCourseTypeName = netCourseTypeName;
	}

	public Integer getNetCourseIntegral() {
		return netCourseIntegral;
	}

	public void setNetCourseIntegral(Integer netCourseIntegral) {
		this.netCourseIntegral = netCourseIntegral;
	}

	public Integer getUserId() {
		return userId;
	}

	public void setUserId(Integer userId) {
		this.userId = userId;
	}
}
